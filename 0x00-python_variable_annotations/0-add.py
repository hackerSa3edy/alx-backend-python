#!/usr/bin/env python3
"""Basic annotations - add

This module provides a function to add two floating-point numbers.
"""

def add(a: float, b: float) -> float:
    """Add two numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of the two numbers.
    """
    return a + b
